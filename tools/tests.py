# 远程上传代码到github，如何实现
# 使用git命令将代码上传到github仓库
# 1. 首先，在本地项目文件夹中初始化git仓库，使用命令：git init
# 2. 将代码添加到git仓库，使用命令：git add .
# 3. 提交代码到git仓库，使用命令：git commit -m "Initial commit"
# 4. 创建一个远程仓库，例如在github上创建一个新的仓库
# 5. 将本地仓库与远程仓库关联，使用命令：git remote add origin <远程仓库URL>
# 6. 将本地代码推送到远程仓库，使用命令：git push -u origin master


